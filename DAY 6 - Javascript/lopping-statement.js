//Control Flow Structure
//Looping statement

//For loop
for(let i = 1; i <= 5; i++) {
    console.log("For loop count:", i);
}

//while loop
let count = 1;
while(count <= 5){
    console.log(count);
    count ++;
}

//Do while
let a = 4;
do{
    console.log("Do while count: ",a)
    a++
} while(a <= 5);

